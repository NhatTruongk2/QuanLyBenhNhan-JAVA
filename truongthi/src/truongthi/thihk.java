    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        int stt = -1;
        benhan benhNhanMaxVienPhi = null;
        benhnhan benhNhanMaxVienPhiBN = null;

        for (int i = 0; i < benhanList.size(); i++) {
            stt++;
            benhan BA = benhanList.get(i);
            benhnhan BN = benhnhanList.get(i);

            if (benhNhanMaxVienPhi == null || BA.getVienPhi() > benhNhanMaxVienPhi.getVienPhi()) {
                benhNhanMaxVienPhi = BA;
                benhNhanMaxVienPhiBN = BN;
            }
        }

        if (benhNhanMaxVienPhi != null) {
            List<benhan> ba = new ArrayList<>();
            List<benhnhan> bn = new ArrayList<>();

            ba.add(benhNhanMaxVienPhi);
            bn.add(benhNhanMaxVienPhiBN);

            // Xóa danh sách hiển thị hiện tại
            hienThilenTable(bn, ba);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

