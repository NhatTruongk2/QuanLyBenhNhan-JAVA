    public void hienThilenTable(List<benhnhan> list, List<benhan> list2) {
        DefaultTableModel tableModel = (DefaultTableModel) tbl1.getModel();
        // xoas trang table 
        tableModel.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            tableModel.addRow(new Object[]{tableModel.getRowCount() + 1,
                list.get(i).getMaBN(), list.get(i).getTenBN(), list.get(i).getDiaChi(), list.get(i).getDienThoai(),
                list.get(i).getGioiTinh(), list.get(i).getBHYT(), list.get(i).getNgaySinh(),
                list.get(i).getThangSinh(), list.get(i).getNamSinh(),
                list2.get(i).getMaBA(), list2.get(i).getTenBA(), list2.get(i).getKhoa(),
                list2.get(i).getPhong(), list2.get(i).getNgayVao(), list2.get(i).getThangvao(),
                list2.get(i).getNamVao(), list2.get(i).getNgayRa(), list2.get(i).getThangRa(),
                list2.get(i).getNamRa(), list2.get(i).getVienPhi()
            });
        }

    }

